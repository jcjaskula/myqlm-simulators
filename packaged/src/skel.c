#include <stdlib.h>
#include <stdio.h>
#include <string.h>


//*********************************************************************************
//********************************** Defines **************************************
//*********************************************************************************

// Sample string
#define WORLD ", World!"


//*********************************************************************************
//********************************** Types ****************************************
//*********************************************************************************


//*********************************************************************************
//********************************** Static Functions *****************************
//*********************************************************************************

static char* _get_str();


//*********************************************************************************
//********************************** Global Variables *****************************
//*********************************************************************************

const char* HELLO = "Hello";


//*********************************************************************************
//********************************** Implementation *******************************
//*********************************************************************************

char * get_skel_str() {
    return _get_str();
}

//*********************************************************************************
//********************************** Static Helpers Implementation ****************
//*********************************************************************************

char * _get_str() {
    char * str = malloc(14 * sizeof(char));
    if (NULL == str) {
        fprintf(stderr, "A problem occured during malloc for the Hello string!\n");
        exit(1);
    }

    int rc = sprintf(str, "%s%s\n", HELLO, WORLD);
    if (0 != rc) {
        fprintf(stderr, "A problem occured while constructing the Hello string!\n");
        exit(1);
    }

    return str;
}

